public void cleanTable()
{
        try {
            DefaultTableModel modelo=(DefaultTableModel) Table.getModel();
            int filas=Table.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
}   

