    public void fillTable(ArrayList<ArrayList<String>> onDesk)
    {
        
        model = (DefaultTableModel)Table.getModel();
        for(int i=0;i<=onDesk.size()-1;i++) model.addRow(new Object[]{onDesk.get(i).get(0),onDesk.get(i).get(1)});
    }
